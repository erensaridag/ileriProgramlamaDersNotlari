
# range(): Belli bir aralıkta bulunan sayıları göstermek için kullanılır.
# Kullanım biçimleri:
# range(ilk_sayı, son_sayı)       -> Belirtilen ilk ve son sayı arasındaki sayıları üretir (son sayı dahil değildir)
# range(ilk_sayı, son_sayı, adım) -> Belirtilen aralıkta, adım değerine göre sayıları üretir

# Örnekler:

# 1-9 arasındaki sayıları yazdırır (10 dahil değil)
# for i in range(1, 10):
#     print(i)

# 0-9 arasındaki sayıları yazdırır (10 dahil değil)
# for i in range(10):
#     print(i)

# 0-8 arasındaki sayıları 2'şer atlayarak yazdırır (10 dahil değil)
# for i in range(0, 10, 2):
#     print(i)

# 10-1 arasındaki sayıları geriye doğru yazdırır (0 dahil değil)
# for i in range(10, 0, -1):
#     print(i)
